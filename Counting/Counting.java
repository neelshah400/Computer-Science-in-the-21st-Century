public class Counting{
	public static void main(String[]args){

		int counter = 0;
		counter = counter +1;
		System.out.println(counter);
		counter ++;
		System.out.println(counter);
		counter += 1;
		System.out.println(counter);
		counter += 5007;
		System.out.println(counter);
		counter --;
		counter -= 10;
		counter *= 100;
		counter /= 5;
		counter %= 7;

	}
}