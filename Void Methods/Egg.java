public class Egg{
	public static void main(String[]args){

		EggTop();
		EggMiddle();
		EggBottom();

	}

	public static void EggTop(){

		System.out.println("  _______");
		System.out.println(" /       \\");
		System.out.println("/         \\");

	}

	public static void EggMiddle(){

		System.out.println("-\"-\'-\"-\'-\"-");

	}

	public static void EggBottom(){

		System.out.println("\\         /");
		System.out.println(" \\_______/");

	}
}